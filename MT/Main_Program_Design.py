    def read_file(self, filename):
        with open(filename, "r") as file:
            prices = file.readlines()
        return prices

    def update_prices(self, List, filename):
        data_prices = self.read_file(filename)
        for index in range(0, len(List)):
            List[index].setText(data_prices[index])

    def update_all(self):
        self.update_prices(self.Pork_Price_List, "Pork_Prices.txt")
        self.update_prices(self.Beef_Price_List, "Beef_Prices.txt")
        self.update_prices(self.Chicken_Price_List, "Chicken_Prices.txt")

    def reset_quantity(self):        
        for quantity in self.Pork_Quantity_List:
            quantity.setValue(0)
        for quantity in self.Beef_Quantity_List:
            quantity.setValue(0)
        for quantity in self.Chicken_Quantity_List:
            quantity.setValue(0)
        self.Total_Amount_Display.setText(str(0.0))
        self.Change_Output.setText("0.0")
        self.Received_Input.setText("")

    def Total_Amount(self):
        total = 0.0
        for index in range(0, len(self.Pork_Quantity_List)):
            total += self.Pork_Quantity_List[index].value() * float(self.Pork_Price_List[index].text())
        for index in range(0, len(self.Beef_Quantity_List)):
            total += self.Beef_Quantity_List[index].value() * float(self.Beef_Price_List[index].text())
        for index in range(0, len(self.Chicken_Quantity_List)):
            total += self.Chicken_Quantity_List[index].value() * float(self.Chicken_Price_List[index].text())
        self.Total_Amount_Display.setText(str(total))

    def Calculate(self):
        try:
            if float(self.Received_Input.text()) >= float(self.Total_Amount_Display.text()):
                self.Change_Output.setText(str(float(self.Received_Input.text()) - float(self.Total_Amount_Display.text())))
            else:
                self.Change_Output.setText("Insufficient Funds!")
        except:
            pass


